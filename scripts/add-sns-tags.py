def add_sns_tags():
    """JSONファイルにSNS向けキャッチコピーとハッシュタグを追加"""
    json_path = "yinyang-app/src/data/characters.json"
    
    # JSONファイルを読み込み
    with open(json_path, 'r', encoding='utf-8') as f:
        characters = json.load(f)
    
    # 各キャラクターにSNSデータを追加
    for char in characters:
        char_id = char['id']
        if char_id in sns_data:
            catchphrase, hashtags = sns_data[char_id]
            char['sns_catchphrase'] = catchphrase
            char['hashtags'] = hashtags
    
    # JSONファイルに書き戻し
    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(characters, f, ensure_ascii=False, indent=2)
    
    print(f"✓ {len(characters)}キャラにSNS向けキャッチコピーとハッシュタグを追加しました")

if __name__ == "__main__":
    add_sns_tags()
